<div class="row">

  <h2>Why CodeIgniter?</h2>

  <br>

  <div class="col-sm-12 col-md-6 features">
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-star-empty"></span>
      </div>
      <h5>Framework with a small footprint</h5>
      <p>CodeIgniter 3 has a 2MB download, including the user guide.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-book"></span>
      </div>
      <h5>Clear documentation</h5>
      <p>The CodeIgniter User Guide comes with the download. It contains an introduction, tutorial, a number of "how to" guides, and then reference documentation for the components that make up the framework.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-hdd"></span>
      </div>
      <h5>Compatibility with standard hosting</h5>
      <p>CodeIgniter 3 only needs PHP 5.2.4, and plays nicely with almost all shared or dedicated hosting platforms. Many webapps need a database, and CodeIgniter supports the most common, including MySQL.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-road"></span>
      </div>
      <h5>No restrictive coding rules</h5>
      <p> Use your own coding and naming conventions, with only a few caveats that deal with classname conflicts. CodeIgniter looks to empower you, not shackle you.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-send"></span>
      </div>
      <h5>Simple solutions over complexity</h5>
      <p>CodeIgniter encourages MVC, but does not force it on you.</p>
    </div>
    
  </div>

  <div class="col-sm-12 col-md-6 features">
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-dashboard"></span>
      </div>
      <h5>Exceptional performance</h5>
      <p>CodeIgniter consistently outperforms most of its competitors.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-ok"></span>
      </div>
      <h5>No large-scale monolithic libraries</h5>
      <p>CodeIgniter is not trying to be all things to all people. It is a lean MVC framework, with enough capabilities to improve your productivity, while providing for third-party addons/plugins for additional functionality.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-list-alt"></span>
      </div>
      <h5>Nearly zero configuration</h5>
      <p> Much of the CodeIgniter configuration is done by convention, for instance putting models in a "models" folder. There are still a number of configuration options available, through scripts in the "config" folder.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-leaf"></span>
      </div>
      <h5>No need for template language</h5>
      <p> CodeIgniter comes with a simple, substitution based, templating tool. Addons/plugins are available for most of the full-blown templating engines, if that is what you are used to.</p>
    </div>
    
    <div class="feature">
      <div class="feature-icon">
        <span class="glyphicon glyphicon-thumbs-up"></span>
      </div>
      <h5>Spend more time away from the computer</h5>
      <p>Don't we all want it? CodeIgniter is easy to learn and to get proficient with.</p>
    </div>
    
  </div>

</div>